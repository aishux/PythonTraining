## 3.3 Assignment Operators
# Assignment operators are used to assign values to variables. 
# You can also combine assignment with arithmetic operations.

# Assign a value
x = 10

# Add and assign
x += 5  # Same as x = x + 5, result is 15

# Subtract and assign
x -= 3  # Same as x = x - 3, result is 12

# Multiply and assign
x *= 2  # Same as x = x * 2, result is 24
