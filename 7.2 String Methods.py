## 7.2 String Methods
# Python provides several built-in methods to manipulate strings:

# upper(): Converts the string to uppercase.
print(my_string.upper())  


# lower(): Converts the string to lowercase.
print(my_string.lower())  


# replace(old, new): Replaces part of a string with another.
print(my_string.replace("H", "J"))  


# strip(): Removes whitespace from the beginning and end of the string.
another_string = "  Hello  "
print(another_string.strip())  
