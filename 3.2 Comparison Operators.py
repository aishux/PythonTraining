## 3.2 Comparison Operators

# Comparison operators compare two values and return True or False depending on the condition.
# These operators are often used in conditional statements to make decisions in your program.

# Equal to
x = 5
y = 3
print(x == y)  

# Not equal to
print(x != y)  

# Greater than
print(x > y)  

# Less than
print(x < y) 

# Greater than or equal to
print(x >= 5)  

# Less than or equal to
print(y <= 3)  
