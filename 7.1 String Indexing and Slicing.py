## 7.1 String Indexing and Slicing
# Indexing: Access individual characters in a string using their position (starting from 0).
my_string = "Hello"
print(my_string[0])  
print(my_string[-1])   # negative indexing starts from the end


#Slicing: Extract parts of a string using start and end indices.
print(my_string[1:4])   # start index inclusive, end index exclusive
print(my_string[:3])   
print(my_string[2:])   
