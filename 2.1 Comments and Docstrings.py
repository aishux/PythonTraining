## 2.1 Comments and Docstrings

# Comments and docstrings are used to make your code more understandable for yourself and others. 
# Python ignores comments when running code.


# Comments: Single-line comments start with a #.
print("Hello, World!")  # This prints a message


# Docstrings: These are multi-line comments that are enclosed in triple quotes (""" """).

"""
This program prints a greeting message.
"""
