## 3.1 Arithmetic Operators
# Arithmetic operators are used to perform basic mathematical operations like addition, subtraction, multiplication, etc.

#Examples:

a = 10
b = 5

# Addition
sum_result = a + b  # 15

# Subtraction
diff_result = a - b  # 5

# Multiplication
prod_result = a * b  # 50

# Division
div_result = a / b  # 2.0 (float)

# Floor Division
floor_div_result = a // b  # 2

# Modulus (remainder)
mod_result = a % b  # 0

# Exponentiation
exp_result = a ** 2  # 100 (10 to the power of 2)
